public class KonversiSuhu {
    public double celciusToFahrenheit(double celsius) {
        return (celsius * 9 / 5) + 32;// membuat method dengan menggunakan rumus convert C째 ke F째
    }
    public double celciusToReamur(double celsius) {
        return celsius * 4 / 5;// membuat method dengan menggunakan rumus convert C째 ke R째
    }
}
